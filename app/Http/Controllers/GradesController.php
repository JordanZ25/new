    {
        $val = $request->id;

        $studentInfo = students::find($val);


        return view('schools.grades.grades')
            ->with('info',$studentInfo);

    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $grades = new grades();

        $grades->id_student = $request->id_student;
        $grades->mathExam= $request->mathExam;
        $grades->math= $request->math;
        $grades->bgExam= $request->bgExam;
        $grades->bg= $request->bg;
        $grades->save();


        return view('schools.schools');
    }
